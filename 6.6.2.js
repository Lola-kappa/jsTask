const arr = [1, 2, 3, 1, 5, 4, 2, 3, 5, 'they', 'don\'t', 'know', 'that', 'we', 'know', 'that', 'they', 'know'];
const new_arr = new Set(arr);
//console.log(new_arr)
for (let i = 0; i > 0; i++) {
    if (new_arr.has[i] = true) {
        new_arr.delete[i]
    } else {
        new_arr.add[i]
    }
}
